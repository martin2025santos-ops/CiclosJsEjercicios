// A) Mostrar números desde 0 hasta 10, avanzando de 3 en 3
for (let numero = 0; numero <= 10; numero += 3) {
    console.log(numero);
}

// B) Mostrar números desde 0 hasta -10, retrocediendo de 3 en 3
for (let numero = 0; numero >= -10; numero -= 3) {
    console.log(numero);
}